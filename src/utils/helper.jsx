const emojis = ["🔥", "😂", "😍", "👏", "💯", "👍", "😎", "🤯", "🥳", "❤️"];

export const getRandomMessage = () => {
  const wordCount = Math.floor(Math.random() * 9) + 2; // 2–10 words

  let message = [];

  for (let i = 0; i < wordCount; i++) {
    const randomWord = words[Math.floor(Math.random() * words.length)];
    message.push(randomWord);
  }

  // Capitalize first word (optional but looks real)
  message[0] = message[0][0].toUpperCase() + message[0].slice(1);

  const sentence = message.join(" ");

  // Random emoji
  const emoji = emojis[Math.floor(Math.random() * emojis.length)];

  return sentence + " " + emoji;
};
